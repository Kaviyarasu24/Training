number = -10
absolute_value = abs(number)
print(f"Absolute value of {number} is {absolute_value}")

dividend = 10
divisor = 3
quotient, remainder = divmod(dividend, divisor)
print(f"Quotient of {dividend} / {divisor} is {quotient} and remainder is {remainder}")

numbers = [1, 2, 3, 4, 5]
minimum_value = min(numbers)
maximum_value = max(numbers)
print(f"Minimum value in {numbers} is {minimum_value}")
print(f"Maximum value in {numbers} is {maximum_value}")

number_to_round = 3.14159
rounded_number = round(number_to_round, 2)
print(f"{number_to_round} rounded to 2 decimal places is {rounded_number}")

container = [1, 2, 3, 4, 5]
number_of_items = len(container)
print(f"Number of items in the container is {number_of_items}")